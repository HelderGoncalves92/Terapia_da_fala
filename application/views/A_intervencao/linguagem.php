		<p>As etapas de desenvolvimento abaixo descritas são uma das diversas propostas existentes, que permitem perceber se a criança se está a desenvolver de acordo com etapas apresentadas.</p>
		<p>É importante referir que cada criança tem um desenvolvimento único, podendo diferenciar-se das outras nalguns aspectos, no entanto, isso não significa que esta tenha algum problema.</p>

		<div id='table-linguagem'>
			<table class='table table-striped table-bordered'>
				<tr>
					<th>Idade</th>
					<th>Linguagem Expressiva</th>
					<th>Linguagem Receptiva</th>
				</tr>
				<tr>
					<th>0 - 3 Meses</th>
					<td>Produz sons manifestando satisfação, Palreio; Apresenta choro diferenciado para expressar necessidades; Vocaliza em resposta ao estímulo e sorriso do adulto.</td>
					<td>Reage aos sons; Localiza a fonte sonora; Acalma-se ou sorri quando ouve uma voz familiar; Reconhece a voz dos pais.</td>
				</tr>
				<tr>
					<th>4 – 6 Meses</th>
					<td>Emite sons sozinho ou com interação (papapa, mamama) – lalação; Imite os sons que ouve; Expressa alegria ou tristeza nas suas produções.</td>
					<td>Reage ao nome; Reconhece os familiares; Identifica brinquedos e objectos comuns; Reage a diferentes entoações e sons.</td>
				</tr>
				<tr>
					<th>7 – 12 Meses</th>
					<td>Vocaliza para chamar a atenção; Repete sons, sílabas e palavras; Começa a surgir a ecolália; Surgem as primeiras palavras (mamã, papá, não, bo/ “bola”).</td>
					<td>Identifica familiares, comidas e animais; Compreende ordens simples (ex: dá, diz adeus); Compreende verbos de acções (ex: brincar, comer); identifica imagens e algumas partes do corpo.</td>
				</tr>
				<tr>
					<th>1 – 2 Anos</th>
					<td>Aumenta o seu vocabulário ativo (50 a 200 palavras); Utiliza a holófrase (palavra com sentido de frase “água” para “quero beber água”); Faz perguntas simples (“papá rua?”); Utiliza mais sons consonânticos; Usa palavras mais corretas; Imita os sons dos animais; </td>
					<td>Conhece a função de objetos mais comuns; Compreende perguntas simples (- tens fome?); Compreende ordens mais complexas ( - chama o papá para comer!); Gosta de ouvir histórias, canções e rimas (ex: “tão balalão, cabeças de cão”).</td>
				</tr>
				<tr>
					<th>2 – 3 Anos</th>
					<td>Nomeia tudo o que pretende; usa frases com 2 e 3 palavras de conteúdo – frases telegráficas; Exibe uma expressão verbal compreensível para os familiares; Faz perguntas simples; Podem surgir hesitações nas palavras ou repetições silábicas</td>
					<td>Compreende ordens mais complexas (ex: pega no bola e põe-a na caixa!); Identifica conceitos opostos (ex: frio/quente); Identifica imagens de acções.</td>
				</tr>
				<tr>
					<th>3 – 4 Anos</th>
					<td>Expressão oral compreensível  – conta acontecimentos, usa frases mais complexas e corretas; Sabe dizer o nome, a idade e a morada; Usa os plurais, os pronomes e os verbos no passado; Pode apresentar dificuldades na articulação de alguns fonemas;</td>
					<td>Compreende perguntas com: “onde?”, “quem?”, “o quê?”; Responde a perguntas sobre histórias simples.</td>
				</tr>
				<tr>
					<th>4 – 5 Anos</th>
					<td>Utiliza frases com 6 a 8 palavras com detalhes e gramaticalmente corretas, conta histórias e acontecimentos; Pergunta o significado do vocabulário desconhecido; Define palavras e conhece alguns antónimos; Articula correctamente os fonemas em geral.</td>
					<td>Compreende ordens mais complexas (ex: pega no lápis azul e põe dentro da caixa!); Gosta de ouvir histórias e responde a perguntas; Compreende o discurso coloquial da família e do meio envolvente; Adquiriu a noção de antónimo (ex: o contrário de grande é… ).</td>
				</tr>
