		<h1 class='title text-center'><span class="text-muted">O que é a </span>Fluência?</h1>
		<hr>
		<p>Entende-se por fluência um discurso em que não seja notório nenhuma alteração, sendo este caracterizado como suave e fluído. </p>
		<p>Quando existem alterações na fluência está-se perante um quadro de gaguez, sendo esta caracterizada pela ocorrência frequente de um ou mais dos seguintes fenómenos:</p>
		<ul class='lista'>
			<li>Repetições de sons e sílabas</li>
			<li>Prolongamentos de sons</li>
			<li>Interjeições</li>
			<li>Palavras Segmentadas</li>
			<li>Bloqueios</li>
			<li>Circunlóquios (utilização de várias palavras quando podia ter utilizado uma)</li>
		</ul>
		<p>Além da gaguez, também a taquifémia tem como característica principal uma alteração na fluência. Mas se a primeira tem como particularidade um discurso segmentado, a segunda tem como factor distintivo o facto de a fluência se encontrar afectada devido à rapidez do discurso, tornando-se muitas vezes imperceptível a quem escuta.</p>




